package com.javaclass;

public class Switch_Case {
public static void main(String[] args) {
	int a=110;
	switch (a) {
	case 10:
		System.out.println("ten");
		break;
	case 30:
		System.out.println("thirty");
		break;
	case 60:
		System.out.println("sixty");
		break;
	default:
		System.out.println("invalid entry");
		break;
	}
}
}
